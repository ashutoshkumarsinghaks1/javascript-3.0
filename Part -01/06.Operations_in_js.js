// Operations in Js

a = 20;
b = 10;

// addition
sum = a + b;
console.log(sum); // 30

// subtraction
diff = a - b;
console.log(diff); // 10

// multiplication
prod = a * b;
console.log(prod); // 200

// division
div = a / b;
console.log(div); //  2

// modulo
mod = a % b;
console.log(mod); // 0

// Exponential (power operator)
exponent = a ** b;
console.log(exponent); // 10240000000000
