// Operator Precedence
//This is general order of solving an expression
// () -> ** -> *,/,%  -> + ,-

a = (5 + 2) / 7 + 1 * 2;
console.log(a); // 3

// Evaluate
/*
1.(2+ 1)*3
2. 3 / 3 + 2 ** 2
3. 4 + 1 * 6/2

*/

a1 = (2 + 1) * 3;
console.log(a1); // 9

a2 = 3 / 3 + 2 ** 2;
console.log(a2); // 5

a3 = 4 + (1 * 6) / 2;
console.log(a3); // 7
